export function searchPlants(query: string): PlantSpecies[] {
  const lowerQuery = query.toLowerCase();
  return PLANT_SPECIES.filter(
    plant =>
      plant.common_name.toLowerCase().includes(lowerQuery) ||
      plant.botanical_name.toLowerCase().includes(lowerQuery) ||
      plant.family.toLowerCase().includes(lowerQuery) ||
      plant.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}

export function getPlantById(id: string): PlantSpecies | undefined {
  return PLANT_SPECIES.find(p => p.id === id);
}

export function getPlantsByDifficulty(difficulty: PlantSpecies['difficulty']): PlantSpecies[] {
  return PLANT_SPECIES.filter(p => p.difficulty === difficulty);
}

export function getPetSafePlants(): PlantSpecies[] {
  return PLANT_SPECIES.filter(p => !p.toxic_pets);
}

export function getPlantsByTag(tag: string): PlantSpecies[] {
  return PLANT_SPECIES.filter(p => p.tags.includes(tag));
}

export function getAllTags(): string[] {
  const tagSet = new Set<string>();
  PLANT_SPECIES.forEach(p => p.tags.forEach(t => tagSet.add(t)));
  return Array.from(tagSet).sort();
}
